namespace ILWrapper.Enums
{
	/// <summary>
	/// Placement position of an image.
	/// </summary>
	public enum ImagePlacement
	{
		/// <summary>
		/// Position is in the lower left corner.
		/// </summary>
		LowerLeft = 0x701,

		/// <summary>
		/// Position is in the lower right corner.
		/// </summary>
		LowerRight = 0x702,

		/// <summary>
		/// Position is in the upper left corner.
		/// </summary>
		UpperLeft = 0x703,

		/// <summary>
		/// Position is in the upper right corner.
		/// </summary>
		UpperRight = 0x704,

		/// <summary>
		/// Position is in the center.
		/// </summary>
		Center = 0x705,
	}
}
